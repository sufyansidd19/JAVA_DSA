package Recursion; //function calling itself is Recursion


// Why Recursion ? To solve bigger / complex problems in simpler way .

// Base Condition in Recursion where our recursion will
// stop making new calls
public class Main {
    public static void main(String[] args) {
        // Write a function that prints hello world
    message();
    }
    static void message(){
        System.out.println("Hello World");

    }
    // Recursive call
    // If you're calling a function again and again , you can treat it as a separate call in the stack
}

// When we write recursion in general formula we call it recurrence relation.
